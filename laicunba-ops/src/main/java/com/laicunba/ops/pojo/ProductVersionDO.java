package com.laicunba.ops.pojo;

import java.util.Date;

import org.springframework.stereotype.Component;

import com.laicunba.ops.annotation.MyTable;

@Component
@MyTable
public class ProductVersionDO extends BaseDO {
	private String productId;
	private String versionCode;
	private String versionName;
	private String productName;
	private Date planPublishDate;
	private Date acturePublishDate;
	private String status;

	public void setProductName(String productName) {
		this.productName = productName;
	}

	public String getProductName() {
		return productName;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getStatus() {
		return status;
	}

	public String getProductId() {
		return productId;
	}

	public String getVersionCode() {
		return versionCode;
	}

	public String getVersionName() {
		return versionName;
	}

	public Date getPlanPublishDate() {
		return planPublishDate;
	}

	public Date getActurePublishDate() {
		return acturePublishDate;
	}

	public void setProductId(String productId) {
		this.productId = productId;
	}

	public void setVersionCode(String versionCode) {
		this.versionCode = versionCode;
	}

	public void setVersionName(String versionName) {
		this.versionName = versionName;
	}

	public void setPlanPublishDate(Date planPublishDate) {
		this.planPublishDate = planPublishDate;
	}

	public void setActurePublishDate(Date acturePublishDate) {
		this.acturePublishDate = acturePublishDate;
	}

}
